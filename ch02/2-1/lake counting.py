n = 10
m = 12

field =     [['W', '.', '.',  '.', '.', '.', '.', '.', '.', 'W', 'W', '.'],
            ['.', 'W', 'W', 'W', '.', '.', '.', '.', '.', 'W', 'W', 'W'],
            ['.', '.', '.', '.', 'W', 'W', '.', '.', '.', 'W', 'W', '.'],
            ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'W', 'W', '.'],
            ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'W', '.', '.'],
            ['.', '.', 'W', '.', '.', '.', '.', '.', '.', 'W', '.', '.'],
            ['.', 'W', '.', 'W', '.', '.', '.', '.', '.', 'W', 'W', '.'],
            ['W', '.', 'W', '.', 'W', '.', '.', '.', '.', '.', 'W', '.'],
            ['.', 'W', '.', 'W', '.', '.', '.', '.', '.', '.', 'W', '.'],
            ['.', '.', 'W', '.', '.', '.', '.', '.', '.', '.', 'W', '.']]

def dfs(x, y):
    field[x][y] = '.'

    for dx in range(-1, 2):
        for dy in range(-1, 2):
            nx = x + dx
            ny = y + dy

            if 0 <= nx and nx < n and 0 <= ny and ny < m and field[nx][ny] == 'W':
                dfs(nx, ny)

    return

res = 0
for i in range(n):
    for j in range(m):
        if field[i][j] == 'W':
            dfs(i, j)
            res += 1

print(res)